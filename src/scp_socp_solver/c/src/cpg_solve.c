// Solve via canonicalization, canonical solve, retrieval
void scp_socp__cpg_solve(){
  // Canonicalize if necessary
  if (scp_socp__Canon_Outdated.c) {
    scp_socp__cpg_canonicalize_c();
  }
  if (scp_socp__Canon_Outdated.A) {
    scp_socp__cpg_canonicalize_A();
  }
  if (scp_socp__Canon_Outdated.b) {
    scp_socp__cpg_canonicalize_b();
  }
  if (scp_socp__Canon_Outdated.G) {
    scp_socp__cpg_canonicalize_G();
  }
  if (scp_socp__Canon_Outdated.h) {
    scp_socp__cpg_canonicalize_h();
  }
  if (!scp_socp__qoco_solver ) {
    scp_socp__cpg_copy_all();
   QOCOCscMatrix* P = (QOCOCscMatrix*)malloc(sizeof(QOCOCscMatrix));
   QOCOCscMatrix* A = (QOCOCscMatrix*)malloc(sizeof(QOCOCscMatrix));
   QOCOCscMatrix* G = (QOCOCscMatrix*)malloc(sizeof(QOCOCscMatrix));
   P = NULL;
   qoco_set_csc(A, 501, 1564, scp_socp__Canon_Params.A->nnz, scp_socp__Canon_Params.A->x, scp_socp__Canon_Params.A->p, scp_socp__Canon_Params.A->i);
   qoco_set_csc(G, 2720, 1564, scp_socp__Canon_Params.G->nnz, scp_socp__Canon_Params.G->x, scp_socp__Canon_Params.G->p, scp_socp__Canon_Params.G->i);
   QOCOSettings* qoco_settings = (QOCOSettings*)malloc(sizeof(QOCOSettings));
   set_default_settings(qoco_settings);
   scp_socp__qoco_solver = (QOCOSolver*)malloc(sizeof(QOCOSolver));
   qoco_setup(scp_socp__qoco_solver, 1564, 2720, 501, P, scp_socp__Canon_Params.c, A, scp_socp__Canon_Params.b, G, scp_socp__Canon_Params.h, 424, 420, (int *) &scp_socp__qoco_q, qoco_settings);
  }
  if (scp_socp__Canon_Outdated.b) {
    update_vector_data(scp_socp__qoco_solver, NULL, scp_socp__Canon_Params.b, NULL);
  }
  if (scp_socp__Canon_Outdated.h) {
    update_vector_data(scp_socp__qoco_solver, NULL, NULL, scp_socp__Canon_Params.h);
  }
  if (scp_socp__Canon_Outdated.G) {
    update_matrix_data(scp_socp__qoco_solver, NULL, NULL, scp_socp__Canon_Params.G->x);
  }
  if (scp_socp__Canon_Outdated.c) {
    update_vector_data(scp_socp__qoco_solver, scp_socp__Canon_Params.c, NULL, NULL);
  }
  if (scp_socp__Canon_Outdated.A) {
    update_matrix_data(scp_socp__qoco_solver, NULL, scp_socp__Canon_Params.A->x, NULL);
  }
  scp_socp__qoco_solver->settings->max_iters = scp_socp__Canon_Settings.max_iters;
  scp_socp__qoco_solver->settings->bisect_iters = scp_socp__Canon_Settings.bisect_iters;
  scp_socp__qoco_solver->settings->ruiz_iters = scp_socp__Canon_Settings.ruiz_iters;
  scp_socp__qoco_solver->settings->iter_ref_iters = scp_socp__Canon_Settings.iter_ref_iters;
  scp_socp__qoco_solver->settings->kkt_static_reg = scp_socp__Canon_Settings.kkt_static_reg;
  scp_socp__qoco_solver->settings->kkt_dynamic_reg = scp_socp__Canon_Settings.kkt_dynamic_reg;
  scp_socp__qoco_solver->settings->abstol = scp_socp__Canon_Settings.abstol;
  scp_socp__qoco_solver->settings->reltol = scp_socp__Canon_Settings.reltol;
  scp_socp__qoco_solver->settings->abstol_inacc = scp_socp__Canon_Settings.abstol_inacc;
  scp_socp__qoco_solver->settings->reltol_inacc = scp_socp__Canon_Settings.reltol_inacc;
  scp_socp__qoco_solver->settings->verbose = scp_socp__Canon_Settings.verbose;
  // Solve with QOCO
  qoco_solve(scp_socp__qoco_solver);
  // Retrieve results
  scp_socp__cpg_retrieve_prim();
  scp_socp__cpg_retrieve_dual();
  scp_socp__cpg_retrieve_info();
  // Reset flags for outdated canonical parameters
  scp_socp__Canon_Outdated.c = 0;
  scp_socp__Canon_Outdated.A = 0;
  scp_socp__Canon_Outdated.b = 0;
  scp_socp__Canon_Outdated.G = 0;
  scp_socp__Canon_Outdated.h = 0;
}

